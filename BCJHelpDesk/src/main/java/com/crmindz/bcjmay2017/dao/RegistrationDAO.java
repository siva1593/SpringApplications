package com.crmindz.bcjmay2017.dao;

import java.util.List;

import com.crmindz.bcjmay2017.pojo.User;

/**
 * @author Siva Reddy. 
 * 		   Acts as an Interface which contains only
 *         Abstract methods Implemented by various classes.
 */
public interface RegistrationDAO {
	
	/**
	 * Stores all the user details in the database.
	 * 
	 * @param users
	 *            It is the entity class that has all the information of user to
	 *            be stored.
	 * @return Returns a string.
	 */
	public String registerUser(User user);
	

	/**
	 * Gets user types from reference table in database.
	 * 
	 * @return Returns a list of map's that has user types.
	 */
	public List<String> getUserType();
	

	
	/**
	 * Gets states from reference table in database.
	 * 
	 * @return Returns a list of map's that has states.
	 */
	public List<String> getStates();

}
